package com.winky.expand.view.layout.auto;

/**
 * @author winky
 * @date 2018/5/26
 */
public class AutoParam {

    private int attr;
    private int value;

    public AutoParam(int attr, int value) {
        this.attr = attr;
        this.value = value;
    }

    public int getAttr() {
        return attr;
    }

    public int getValue() {
        return value;
    }
}
